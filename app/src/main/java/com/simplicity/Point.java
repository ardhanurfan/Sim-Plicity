public class Point {
    private int x = 0;
    private int y = 0;
    // a constructor!
    public Point(int a, int b) {
    x = a; y = b;
    }

    public int getX(){
        return x;
    }

    public int getY(){
        return y;
    }

    public void setX(int ax){
        x=ax;
    }

    public void setY(int ay){
        y=ay;
    }

}

